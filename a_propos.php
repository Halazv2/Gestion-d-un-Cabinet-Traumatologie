          <p style="font-size: 14px;">La traumatologie est la spécialité médicale consacrée aux traumatismes physiques,
            c’est-à-dire l’ensemble des coups, blessures ou chocs subis de façon violente et soudaine.
            La traumatologie est rattachée à la chirurgie orthopédique et la chirurgie traumatologique, puisque les
            blessures graves relèvent de la chirurgie. Elle peut aussi relever de la médecine d’urgence,
            car bon nombre de traumatisés entrent à l’hôpital par cette voie.</p>
